"""
x：公鸡
y：母鸡
3z：小鸡
x + y + 3z = 100
5x+ 3y+ z = 100
300 - 15x - 9y + x + y = 100
7x + 4y = 100
"""

x = 0
while 7 * x < 100:
    if (100 - 7 * x) % 4 == 0:
        y = int((100 - 7 * x) / 4)
        z = 100 - 5 * x - 3 * y
        if z >= 0:
            print(f'鸡翁：{x}, 鸡母：{y}, 鸡雏：{3 * z}')
    x += 1
